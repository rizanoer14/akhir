    protected void DataTablePemasok(){
        //Object[] baris = {"ID Pemasok","Nama","Alamat","No.Telepon"};
        tabmode = (DefaultTableModel) tblpemasok.getModel();
        tblpemasok.setModel(tabmode);
        String sql = "SELECT * from datapemasok";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery (sql);
            while (hasil.next()){
                String a = hasil.getString("id_pemasok"); //isi tabel database
                String b = hasil.getString("nm_pemasok");
                String c = hasil.getString("alamat");
                String d = hasil.getString("notelp");
 
                String[]data = {a,b,c,d};
                tabmode.addRow(data);
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }
    
