    private void txtcaripemasokKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtcaripemasokKeyReleased
        tabmode = (DefaultTableModel) tblpemasok.getModel();
        tblpemasok.setModel(tabmode);
        String sql = "select * from datapemasok where id_pemasok like '%"+txtcaripemasok.getText()+"%'"
        + "or nm_pemasok like '%"+txtcaripemasok.getText()+"%'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while (hasil.next()){
                String a = hasil.getString("id_pemasok");
                String b = hasil.getString("nm_pemasok");
                String [] data = {a,b};
                tabmode.addRow(data);
            }
        }catch (Exception e){
        }
    }//GEN-LAST:event_txtcaripemasokKeyReleased

