    private void bttambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttambahActionPerformed
        SimpleDateFormat ft = new SimpleDateFormat ("yyyy-MM-dd");
        String tanggal = ft.format(tgl.getDate());
           String sql = "INSERT INTO transaksiBeli VALUES (?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtno.getText())
                    ;
            stat.setString(2, txtkodebarang.getText());
            stat.setInt(3,Integer.valueOf(jumlah.getValue().toString()));
            stat.setString(4, txtid.getText());
            stat.setString(5, tanggal);
            int subtotal;
            subtotal = Integer.parseInt(harga.getText())*Integer.valueOf(jumlah.getValue().toString());
            stat.setInt(6, subtotal);
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data Berhasil Disimpan");
            kosong();
            DataTableTransaksi();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Data Gagal Disimpan "+e);
        }
    }//GEN-LAST:event_bttambahActionPerformed

