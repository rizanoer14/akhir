    protected void DataTableTransaksi(){
        Object[] baris = {"Nomor","Kode Barang","Nama Barang","Harga","Jumlah"
                ,"Id Pemasok","Nama Pemasok","Tanggal"};
        tabmode = new DefaultTableModel(null, baris);
        tbltransaksi.setModel(tabmode);
        String sql = "SELECT t.nomor, t.kd_barang, t.tanggal, t.jumlah, b.nm_barang, "
                    + "b.hrg_beli, t.id_pemasok, p.nm_pemasok "
                    + "FROM transaksiBeli AS t, databarang AS b, datapemasok AS p "
                    + "WHERE t.kd_barang = b.kd_barang AND t.id_pemasok = p.id_pemasok "
                    + "AND t.nomor = '"+txtno.getText()+"'";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery (sql);
            while (hasil.next()){
                String a = hasil.getString("nomor"); //isi tabel database
                String b = hasil.getString("kd_barang");
                String c = hasil.getString("nm_barang");
                String d = hasil.getString("hrg_beli");
                String e = hasil.getString("jumlah");
                String f = hasil.getString("id_pemasok");
                String g = hasil.getString("nm_pemasok");
                String h = hasil.getString("tanggal");
                
                String[]data = {a,b,c,d,e,f,g,h};
                tabmode.addRow(data);
            }
        } catch (Exception e){
            e.printStackTrace();
        }
    }
    
    public void NomorFaktur(){
        try{
            String sql = "SELECT nomor FROM transaksiBeli ORDER BY nomor DESC";
            Statement st=conn.createStatement();
            ResultSet rs=st.executeQuery(sql);
                if(rs.next()){
                    String kode = rs.getString("nomor").substring(2);
                    String AN = ""+(Integer.parseInt(kode)+1);
                    String Nol = "";
                    
                    if(AN.length()==1){
                        Nol="000";
                    }else if(AN.length()==2){
                        Nol="00";
                    }else if(AN.length()==3){
                        Nol="0";
                     }else if(AN.length()==4){
                        Nol="";
                     }
                    txtno.setText("NO" + Nol + AN);
                }else{
                    txtno.setText("NO0001");
                }   
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
